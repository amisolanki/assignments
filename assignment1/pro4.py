print("The program for performing operations like AND, OR, and NOT")
var1=bool(input("Enter any boolean value for first variable:"))
var2=bool(input("Enter another boolean value for second variable:"))



print("-----------------------------------------------------------------")
print("The answer after performing the AND operation: " , var1 and var2)
print("-----------------------------------------------------------------")

print("-----------------------------------------------------------------")
print("The answer after performing the OR operation: " , var1 or var2)
print("-----------------------------------------------------------------")

print("-----------------------------------------------------------------")
print("The answer after performing the NOT operation for var1: " , not(var1))
print("-----------------------------------------------------------------")

print("-----------------------------------------------------------------")
print("The answer after performing the NOT operation for var2: " , not(var2))
print("-----------------------------------------------------------------") 
